        public static SmAddress LoadByGroup(string GROUP_CODE)
        {
            Dal.DataAccess _dataAccess = null;
            try
            {
                _dataAccess = new Dal.DataAccess();
                _dataAccess.CreateConnection();
                _dataAccess.CreateSQLCommand(" SELECT * FROM SM_ADDRESS WHERE GROUP_ID IN (SELECT * FROM SM_BUYER_SUPPLIER_GROUPS WHERE GROUP_CODE LIKE '" + GROUP_CODE.Trim() + "%' ");
                System.Data.DataSet ds = _dataAccess.ExecuteDataSet();

                SmAddress obj = null;
                if (GlobalTools.IsSafeDataSet(ds))
                {
                    if ((ds.Tables[0].Rows.Count > 0))
                    {
                        obj = new SmAddress();
                        obj.Fill(ds.Tables[0].Rows[0]);
                    }
                }
                return obj;
            }
            catch (System.Exception)
            {
                throw;
            }
            finally
            {
                if ((_dataAccess != null))
                {
                    _dataAccess.Dispose();
                }
            }
        }

