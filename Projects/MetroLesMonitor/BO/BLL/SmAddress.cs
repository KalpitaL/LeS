        public static SmAddressCollection GetAll()
        {
            MetroLesMonitor.Dal.SmAddress dbo = null;
            try
            {
                dbo = new MetroLesMonitor.Dal.SmAddress();
                System.Data.DataSet ds = dbo.SM_ADDRESS_Select_All();
                SmAddressCollection collection = new SmAddressCollection();
                if (GlobalTools.IsSafeDataSet(ds))
                {
                    for (int i = 0; (i < ds.Tables[0].Rows.Count); i = (i + 1))
                    {
                        SmAddress obj = new SmAddress();
                        obj.Fill(ds.Tables[0].Rows[i]);
                        if ((obj != null))
                        {
                            collection.Add(obj);
                        }
                    }
                }
                return collection;
            }
            catch (System.Exception)
            {
                throw;
            }
            finally
            {
                if ((dbo != null))
                {
                    dbo.Dispose();
                }
            }
        }

