        public static SmPdfItemMappingCollection GetAll()
        {
            MetroLesMonitor.Dal.SmPdfItemMapping dbo = null;
            try
            {
                dbo = new MetroLesMonitor.Dal.SmPdfItemMapping();
                System.Data.DataSet ds = dbo.SM_PDF_ITEM_MAPPING_Select_All();
                SmPdfItemMappingCollection collection = new SmPdfItemMappingCollection();
                if (GlobalTools.IsSafeDataSet(ds))
                {
                    for (int i = 0; (i < ds.Tables[0].Rows.Count); i = (i + 1))
                    {
                        SmPdfItemMapping obj = new SmPdfItemMapping();
                        obj.Fill(ds.Tables[0].Rows[i]);
                        if ((obj != null))
                        {
                            collection.Add(obj);
                        }
                    }
                }
                return collection;
            }
            catch (System.Exception)
            {
                throw;
            }
            finally
            {
                if ((dbo != null))
                {
                    dbo.Dispose();
                }
            }
        }

